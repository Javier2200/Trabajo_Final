    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        GeneroF per = new GeneroF();
               
        boolean resultado = per.insertarGenero(Integer.parseInt(txtId.getText()), 
                txtNombre.getText());
        
        if(resultado==true){
            JOptionPane.showMessageDialog(null, "Los Datos Se Insertaron");
        }else{
            JOptionPane.showMessageDialog(null, "Los Datos No Se Insertaron");
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

