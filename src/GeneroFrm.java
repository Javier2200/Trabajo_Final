    private void cargarTabla(){
        GeneroF objGeneroF = new GeneroF();
        ResultSet Resultado = objGeneroF.cargarTablaGenero();
        
        try {
            Object dato [] = new Object[2];
            while (Resultado.next()) {
                for (int i=0; i<2; i++) {
                    dato[i] = Resultado.getObject(i+1);
                }
                modeloTabla3.addRow(dato);
            }
        } catch (Exception e) {
        }
    
    }

